    @Override
    public void actionPerformed(ActionEvent e) {
        clientText = jTextField1.getText();

        // 변수에 저장 후 텍스트필드 초기화
        jTextField1.setText("");

        // 내가 쓴 메세지 출력 -> 상대방은 br.readLine()으로 읽어들임
        pw.println(clientText);
        pw.flush();
    }

    @Override
    public void run() {
        try {
            while ((serverText = br.readLine()) != null) {
                jTextArea1.append(serverText + "\n"); // 상대방이 보낸 문자를 채팅창에 세로로 출력
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
