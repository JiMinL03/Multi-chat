    public void Client(){
        try {
            // 클라이언트 측 소켓 정보 초기화
            // Socket(host, port), host: 접속 서버 IP 주소, port: 서버 포트 번호
            InetAddress ia = null; // Local Host IP Address 가져오기 위한 변수
            ia = InetAddress.getLocalHost(); // 현재 PC의 IP Address 가져오기
            socket = new Socket(ia, 5432);
            System.out.println("s>>>" + socket);

            // ========== Server와 Stream 연결 ===========
            br = new BufferedReader(new InputStreamReader(socket.getInputStream()));

            // PrintWriter 스트림의 autoFlush 기능 활성화
            pw = new PrintWriter(socket.getOutputStream(), true);

        } catch (Exception e) {
            System.out.println("클라이언트접속 오류>>>" + e);
        }

        // Thread 객체 생성, Runnable 인터페이스를 구현하기 때문에 this 작성
        Thread ct = new Thread(this);

        // 클라이언트 스레드 실행 → run() 호출
        ct.start();
    }
